#!/bin/bash
set -e

# Автоматический запуск миграций при каждом старте контейнера — осознанный
# компромисс удобства ("одна команда — рабочее приложение") против риска
# гонки при будущем масштабировании computation на несколько реплик
# (параллельный `alembic upgrade head` от нескольких стартующих контейнеров)
# и повторного запуска на каждом `restart: unless-stopped`. Обсуждали и
# сознательно приняли этот риск ради простоты — перенести в README, когда
# дойдём до его сборки.
poetry run alembic upgrade head

cd src
exec poetry run uvicorn main:app --host 0.0.0.0 --port 8000 "$@"
